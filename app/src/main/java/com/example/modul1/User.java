package com.example.modul1;

public class User {
    private int id;
    private String Nama;
    private String Email;
    private String Nowa;
    private String Alamat;
    private String JenisKelamin;
    private String Tingkat;
    private String Umur;

    public int getId() {return id;}
    public void setId(int id) {this.id =id;}

    public String getNama() { return Nama;}
    public void setNama(String Nama) { this.Nama = Nama;}

    public String getEmail() { return Email;}
    public void setEmail(String Email) { this.Email = Email;}

    public String getNowa() { return Nowa;}
    public void setNowa(String Nowa) { this.Nowa = Nowa;}

    public String getAlamat() { return Alamat;}
    public void setAlamat(String Alamat) { this.Alamat = Alamat;}

    public String getJenisKelamin() { return JenisKelamin;}
    public void setJenisKelamin(String JenisKelamin) { this.JenisKelamin = JenisKelamin;}

    public String getTingkat() { return Tingkat;}
    public void setTingkat(String Tingkat) { this.Tingkat = Tingkat;}

    public String getUmur() { return Umur;}
    public void setUmur(String Umur) { this.Umur = Umur;}
}
